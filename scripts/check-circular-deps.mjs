#!/usr/bin/env node
/**
 * Circular dependency baseline gate.
 *
 * Compares the current set of circular dependencies in src/ (as
 * reported by madge) against a committed baseline snapshot.
 *
 * Usage:
 *   node scripts/check-circular-deps.mjs           # check mode (CI)
 *   node scripts/check-circular-deps.mjs --update  # regenerate baseline
 *
 * Exit codes:
 *   0  no new circular dependencies
 *   1  new circular dependencies introduced (CI must fail)
 *   2  internal error (madge missing, baseline missing, ...)
 *
 * The baseline lives at docs-internal/circular-baseline.json.
 * Cycles are canonicalized (rotated so the lexicographically smallest
 * file is first, direction preserved) before comparison so that
 * cosmetic reorderings do not produce false diffs.
 */
import { spawn } from "node:child_process";
import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, "..");
const baselinePath = resolve(repoRoot, "docs-internal", "circular-baseline.json");

function canonicalizeCycle(cycle) {
  if (!Array.isArray(cycle) || cycle.length === 0) return [];
  let minIdx = 0;
  for (let i = 1; i < cycle.length; i += 1) {
    if (cycle[i] < cycle[minIdx]) minIdx = i;
  }
  return [...cycle.slice(minIdx), ...cycle.slice(0, minIdx)];
}

function cycleKey(cycle) {
  return canonicalizeCycle(cycle).join(" -> ");
}

function runMadge() {
  return new Promise((resolveP, rejectP) => {
    const isWin = process.platform === "win32";
    const cmd = isWin ? "npx.cmd" : "npx";
    const child = spawn(
      cmd,
      ["--yes", "madge", "--circular", "--extensions", "ts", "--json", "src"],
      { cwd: repoRoot, shell: isWin },
    );
    let stdout = "";
    let stderr = "";
    child.stdout.on("data", (d) => { stdout += d.toString(); });
    child.stderr.on("data", (d) => { stderr += d.toString(); });
    child.on("error", rejectP);
    child.on("close", () => {
      try {
        const parsed = JSON.parse(stdout);
        if (!Array.isArray(parsed)) {
          rejectP(new Error(`madge JSON output is not an array (stderr: ${stderr})`));
          return;
        }
        resolveP(parsed);
      } catch (err) {
        rejectP(new Error(`Failed to parse madge JSON: ${err.message}\nstdout head: ${stdout.slice(0, 200)}\nstderr head: ${stderr.slice(0, 200)}`));
      }
    });
  });
}

function loadBaseline() {
  try {
    const raw = readFileSync(baselinePath, "utf8");
    const parsed = JSON.parse(raw);
    if (!Array.isArray(parsed.cycles)) {
      throw new Error("baseline.cycles is not an array");
    }
    return parsed.cycles;
  } catch (err) {
    if (err.code === "ENOENT") return null;
    throw err;
  }
}

function writeBaseline(cycles) {
  const payload = {
    note: "Generated by scripts/check-circular-deps.mjs --update. Do not edit by hand.",
    generatedAt: new Date().toISOString(),
    count: cycles.length,
    cycles: cycles.map(canonicalizeCycle).sort((a, b) => cycleKey(a).localeCompare(cycleKey(b))),
  };
  writeFileSync(baselinePath, JSON.stringify(payload, null, 2) + "\n", "utf8");
}

async function main() {
  const update = process.argv.includes("--update");

  let current;
  try {
    current = await runMadge();
  } catch (err) {
    console.error(`[circular-check] ${err.message}`);
    process.exit(2);
  }

  if (update) {
    writeBaseline(current);
    console.log(`[circular-check] baseline updated: ${current.length} cycles -> ${baselinePath}`);
    process.exit(0);
  }

  const baseline = loadBaseline();
  if (baseline === null) {
    console.error(`[circular-check] baseline not found at ${baselinePath}. Run with --update to create it.`);
    process.exit(2);
  }

  const baselineKeys = new Set(baseline.map(cycleKey));
  const currentKeys = new Set(current.map(cycleKey));

  const added = [...currentKeys].filter((k) => !baselineKeys.has(k)).sort();
  const removed = [...baselineKeys].filter((k) => !currentKeys.has(k)).sort();

  if (removed.length > 0) {
    console.log(`[circular-check] ${removed.length} cycle(s) removed from baseline (good, run --update to refresh):`);
    for (const k of removed) console.log(`  - ${k}`);
  }

  if (added.length > 0) {
    console.error(`[circular-check] FAIL: ${added.length} new circular dependency cycle(s) introduced:`);
    for (const k of added) console.error(`  + ${k}`);
    console.error(`[circular-check] Resolve the new cycles or, if intentional, refresh the baseline with:`);
    console.error(`  node scripts/check-circular-deps.mjs --update`);
    process.exit(1);
  }

  console.log(`[circular-check] OK: ${current.length} cycles, baseline matches.`);
  process.exit(0);
}

main().catch((err) => {
  console.error(`[circular-check] unexpected error: ${err.stack || err.message}`);
  process.exit(2);
});
